#include "libprintf.h"

int		ft_f1(int sp, int zr, char **ptr, va_list ap)
{
	char		c;
	char		*s;
	long int	a;

	c = **ptr;
	s = ft_cnv(ptr, ap);
	a = 0;
	if (c == 'd' || c == 'u' || c == 'i' )
		a = ft_d_u_i(sp, zr, s, c);
	return (a);
}
