#include "libprintf.h"

char	*ft_ctoa(int c)
{
	char *ptr;

	if (c == 0)
		ptr = ft_strdup("");
	else
	{
		ptr = malloc(2);
		ptr[1] = '\0';
		ptr[0] = c;
	}
	return (ptr);
}
