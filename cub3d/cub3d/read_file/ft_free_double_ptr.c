#include "cub3d.h"

void	ft_free_double_ptr(char ***str)
{
	int i;

	i = 0;
	while (str[0][i] != NULL)
		free(str[0][i++]);
	free(str[0][i++]);
	free(str[0]);
	str[0] = NULL;
}
