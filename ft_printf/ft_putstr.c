#include "libprintf.h"

int	ft_putstr(char *s, char c)
{
	int	i;

	if (*s == 0 && c == 'c')
		i = write(1, "\0", 1);
	else
		i = write(1, s, ft_strlen(s));
	ft_free(&s);
	return (i);
}
