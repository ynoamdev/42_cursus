#include "libprintf.h"

int	ft_is_convertion(char c)
{
	if (c == 'c' || c == 's' || c == 'x' || c == 'X' ||
			c == 'p' || c == 'u' || c == 'i' || c == 'd' || c == '%')
		return (1);
	return (0);
}
