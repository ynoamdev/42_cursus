#include "libprintf.h"

int	ft_putstr(char *s)
{
	static int 	i;
	int			j;

	j = ft_strlen(s);
	write(1, s, j);
	i += ft_strlen(s);
	if (i > MAX_INT)
		i = -1;
	ft_free(&s);
	return (i);
}
