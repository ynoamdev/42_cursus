#include "cub3d.h"

int	ft_isnothing(char *line)
{
	int i;

	i = 0;
	while (line[i] == ' ' || line[i] == '\t')
		i++;
	if (line[i] == '\0')
		return (1);
	else
		ft_file_error();
	return (1);
}
