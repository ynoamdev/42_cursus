#include "read_file.h"

void	ft_isnothing(char *line)
{
	int i;

	i = 0;
	while (line[i] == ' ' || line[i] == '\t')
		i++;
	if (line[i] == '\0')
		ft_free(&line);
	else
		ft_file_error();
}
