#include "libprintf.h"

char	*ft_minus(char *s)
{
	int		i;
	int		j;

	i = ft_strlen(s);
	j = -1;
	while (++j < i)
		s[j] = s[j + 1];
	write(1, s, i - 1);
	return (i);
}
