#include "libprintf.h"

void	ft_change(int *space, int *zero, int *dot, char c)
{
	if(*zero < 0 && *dot != 2)
		*zero = 0;
	else if (*zero < 0 && *dot == 2)
	{
		*zero = *space;
		*zero = 0;
	}
}
