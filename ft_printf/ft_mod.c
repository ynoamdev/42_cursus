#include "libprintf.h"

int	ft_mod(int sp, int zr)
{
	int	a;

	a = 0;
	if (zr < 0)
	if (sp > 0)
		a += ft_space(sp - zr - 1);
	if (zr > 0)
		a += ft_zero(zr - 1);
	a += write(1, "%", 1);
	if (sp < 0)
	{
		sp = sp * -1;
		a += ft_space(sp - a);
	}
	return(a);
}
