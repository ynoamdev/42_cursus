#include "libprintf.h"

int	ft_mod(int sp_zr, int j)
{
	int i;
	int a;

	i = 32;
	a = 0;
	if (j == 1)
		i = 48;
	else if (j == 2)
		a += write(1, "%", 1);
	while (sp_zr >= 0 && sp_zr--)
		a += write(1, &i, 1);
	if (j != 2)
		a += write(1, "%", 1);
	return (a);
}
